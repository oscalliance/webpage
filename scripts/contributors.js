function populateProjectContributors(container) {
    const pageId = container.closest('.project-page').id;
    const names = projectContributors[pageId] || [];

    const scrollContainer = container.querySelector('.project-page-contributors-scroll');
    const template = scrollContainer.querySelector('.project-page-contributor');

    scrollContainer.querySelectorAll('.project-page-contributor:not([style*="display: none"])').forEach(el => el.remove());

    const shuffled = [...names].sort(() => Math.random() - 0.5);

    shuffled.forEach(name => {
        const clone = template.cloneNode(true);
        clone.style.display = 'flex';
        clone.querySelector('.contributor-name').textContent = name;
        scrollContainer.appendChild(clone);
    });
}

document.querySelectorAll('.project-page-contributors-container').forEach(container => {
    populateProjectContributors(container);
});
